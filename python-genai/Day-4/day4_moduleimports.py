# Program to demonstrate module imports in Python

# Importing the math module
import math
import random
import datetime

# Using functions from the math module
print("Square root of 16 is:", math.sqrt(16))
print("Value of pi is:", math.pi)   
print("Cosine of 0 is:", math.cos(0))
print("Factorial of 5 is:", math.factorial(5))
print("Logarithm of 1000 to base 10 is:", math.log10(1000))
print("**" * 20)  # Just a separator for better readability
# Using functions from the random module
print("Random number between 1 and 10:", random.randint(1, 10))
print("**" * 20)  # Just a separator for better readability
# Using functions from the datetime module
print("Current date and time:", datetime.datetime.now())
print("**" * 20)  # Just a separator for better readability
